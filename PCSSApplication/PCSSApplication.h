    void loadLight()
    {
        float positions[] = {
                //  (z = -0.5)
                -0.5f, -0.5f, -0.5f, // v0
                0.5f, -0.5f, -0.5f, // v1
                0.5f,  0.5f, -0.5f, // v2
                -0.5f,  0.5f, -0.5f, // v3

                //  (z = 0.5)
                -0.5f, -0.5f,  0.5f, // v4
                0.5f, -0.5f,  0.5f, // v5
                0.5f,  0.5f,  0.5f, // v6
                -0.5f,  0.5f,  0.5f, // v7
        };

        vertices3.clear();
        for(int i = 0; i < 8; ++i){
            VertexLight vertex{};
            vertex.pos = glm::vec3(positions[i * 3], positions[i * 3 + 1], positions[i * 3 + 2]);
            vertices3.push_back(vertex);
        }

        indices3.clear();
        unsigned int ids[] = {
                //  (z = -0.5)
                1, 0, 2,  // 
                3, 2, 0,  // 

                //  (z = 0.5)
                4, 5, 6,  // 
                6, 7, 4,  // 

                //  (x = -0.5)
                0, 4, 7,  // 
                7, 3, 0,  // 

                //  (x = 0.5)
                1, 6, 5,  // 
                6, 1, 2,  // 

                //  (y = -0.5)
                0, 1, 5,  // 
                5, 4, 0,  // 

                //  (y = 0.5)
                2, 3,6,  // 
                6, 3, 7   // 
        };
        for(unsigned int id : ids)
            indices3.push_back(id);
    }

    void loadMarry()
    {
        tinyobj::attrib_t attrib;
        std::vector<tinyobj::shape_t> shapes;
        std::vector<tinyobj::material_t> materials;
        std::string warn, err;

        if (!tinyobj::LoadObj(&attrib, &shapes, &materials, &warn, &err, MARRY_PATH.c_str())) {
            throw std::runtime_error(warn + err);
        }

        std::unordered_map<VertexMarry, uint32_t> uniqueVertices{};

        for (const auto& shape : shapes) {
            for (const auto& index : shape.mesh.indices) {
                VertexMarry vertex{};

                vertex.pos = {
                        attrib.vertices[3 * index.vertex_index + 0],
                        attrib.vertices[3 * index.vertex_index + 1],
                        attrib.vertices[3 * index.vertex_index + 2]
                };

                vertex.texCoord = {
                        attrib.texcoords[2 * index.texcoord_index + 0],
                        1.0f - attrib.texcoords[2 * index.texcoord_index + 1]
                };

                vertex.normal = {
                        attrib.normals[3 * index.normal_index + 0],
                        attrib.normals[3 * index.normal_index + 1],
                        attrib.normals[3 * index.normal_index + 2]
                };

                if (uniqueVertices.count(vertex) == 0) {
                    uniqueVertices[vertex] = static_cast<uint32_t>(vertices.size());
                    vertices.push_back(vertex);
                }

                indices.push_back(uniqueVertices[vertex]);


                /*
                vertices.push_back(vertex);
                indices.push_back(vertices.size() - 1);
                 */
            }
        }
    }

